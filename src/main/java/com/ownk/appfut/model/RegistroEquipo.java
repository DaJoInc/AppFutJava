package com.ownk.appfut.model;

public class RegistroEquipo {
	
	private String		idUsuario;
	private String		idEquipo;
	private	String	    nombreEquipo;
	private String	    estadoEquipo;
	private String	    idDtEquipo;
	private	String		codRespuesta;
	private String		msgRespuesta;
	
	public String getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(String idUsuario) {
		this.idUsuario = idUsuario;
	}
	public String getIdEquipo() {
		return idEquipo;
	}
	public void setIdEquipo(String idEquipo) {
		this.idEquipo = idEquipo;
	}
	public String getNombreEquipo() {
		return nombreEquipo;
	}
	public void setNombreEquipo(String nombreEquipo) {
		this.nombreEquipo = nombreEquipo;
	}
	public String getEstadoEquipo() {
		return estadoEquipo;
	}
	public void setEstadoEquipo(String estadoEquipo) {
		this.estadoEquipo = estadoEquipo;
	}
	public String getIdDtEquipo() {
		return idDtEquipo;
	}
	public void setIdDtEquipo(String idDtEquipo) {
		this.idDtEquipo = idDtEquipo;
	}
	public String getCodRespuesta() {
		return codRespuesta;
	}
	public void setCodRespuesta(String codRespuesta) {
		this.codRespuesta = codRespuesta;
	}
	public String getMsgRespuesta() {
		return msgRespuesta;
	}
	public void setMsgRespuesta(String msgRespuesta) {
		this.msgRespuesta = msgRespuesta;
	}
	
	
	
}
